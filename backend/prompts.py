"""
prompts.py — LangChain 프롬프트 템플릿 정의

REFINE_PROMPT : 사용자 질문 → 검색 키워드 변환 (refine_node에서 사용)
RAG_PROMPT    : 검색된 문서 + 질문 → 최종 답변 생성 (generate_node에서 사용)
"""

from langchain_core.prompts import ChatPromptTemplate

# 사용자 질문을 BM25·벡터 검색에 적합한 짧은 키워드로 변환하는 프롬프트
REFINE_PROMPT = ChatPromptTemplate.from_messages([
    ("system", """\
아래 질문에 답하는 문서를 찾기 위한 검색 쿼리를 생성하세요.

- 핵심 명사와 키워드 위주로 간결하게 작성하세요 (10단어 이내)
- 질문에 없는 내용은 추가하지 마세요.
- 검색 쿼리 텍스트만 출력하세요 (설명, 따옴표 없이)

예시:
  입력: VPN이 갑자기 안 되는데 설정 방법 좀 알려줘
  출력: VPN 연결 오류 설정

  입력: 저 어제부터 회사 네트워크에 연결하면 인증 오류가 뜨는데 어떻게 해결해?
  출력: 회사 네트워크 인증 오류 해결

  입력: 재택근무 신청은 어떻게 하면 돼?
  출력: 재택근무 신청\
"""),
    ("human", "{question}"),
])

# LLM이 참고 문서를 기반으로 최종 답변을 생성할 때 사용하는 시스템 프롬프트
SYSTEM_PROMPT = """\
당신은 사내 Confluence 문서를 기반으로 질문에 답변하는 어시스턴트입니다.
아래 [참고 문서]를 바탕으로 질문에 답변하세요.

[충실성]
- 답변 전에 각 문서가 질문 대상과 직접 관련된 문서인지 확인하세요. 문서 제목이나 내용이 다른 시스템/주제를 다루면 해당 문서는 사용하지 마세요.
- 문서에 명시된 내용만 사실로 전달하세요.
- 문서에 없는 내용을 추론한 경우 "~로 보입니다", "문서에 직접 명시되어 있지는 않지만" 등으로 불확실성을 표현하세요.
- 질문의 전제가 문서에서 확인되지 않으면 그 사실을 먼저 알려주세요.
- 문서에 없는 내용은 "문서에서 찾을 수 없습니다"라고 답변하세요.

[형식]
- 자연스러운 문장으로 답변하세요. 설명은 문장으로, 항목 나열이 필요한 경우에만 리스트를 사용하세요.
- 단순 질문은 간결하게, 상세 요청은 문서 내용을 충분히 활용하여 구조화해서 답변하세요.
- 출처(문서 제목)를 답변 마지막에 명시하세요.
- 한국어로 답변하세요.

[참고 문서]
{context}
"""

RAG_PROMPT = ChatPromptTemplate.from_messages([
    ("system", SYSTEM_PROMPT),
    ("human", "{question}"),
])
